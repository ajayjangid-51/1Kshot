struct Node
{
    int val;
    Node *left;
    Node *right;
    Node *next;
    Node(int x) : val(x), left(NULL), right(NULL), next(NULL) {}
};
void solve()
{
    Node *root = new Node(1);
    root->left = new Node(2);
    root->right = new Node(3);
    root->left->left = new Node(4);
    root->left->right = new Node(5);
    root->right->left = new Node(6);
    root->right->right = new Node(7);

    queue<Node *> q;
    q.push(root);
    while (!q.empty())
    {
        for (int i = 0; i < q.size(); i++)
        {
            Node *t = q.front();
            q.pop();
            if (q.size() > 0)
            {
                t->next = q.front();
            }
            if (t->left)
                q.push(t->left);
            if (t->right)
                q.push(t->right);
        }
    }
    Node *r = root;
    while (r)
    {
        print(r->val);
        if (!r->next)
        {
            r = r->left;
        }
        else
            r = r->next;
    }
}
