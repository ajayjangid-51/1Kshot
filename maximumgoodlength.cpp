int fn(vvi A)
{
    int n = A.length;
    int m = A[0].length;
    int maxLen = 1;
    for (int i = 0; i < n; i++)
    {
        for (int j = 0; j < m; j++)
        {
            // check for all possible matrix of i+len/j+len lengths (square matrices)
            for (int len = 1; i + len - 1 < n && j + len - 1 < m; len++)
            {
                bool good = true;
                // check if all elements >= len
                for (int k = i; k < i + len; k++)
                {
                    for (int l = j; l < j + len; l++)
                    {
                        if (A[k][l] < len)
                        {
                            good = false;
                            break;
                        }
                    }
                    if (!good)
                        break;
                }
                // update maxLen
                if (good)
                    maxLen = Math.max(maxLen, len);
            }
        }
    }
    return maxLen;
}
