struct TreeNode
{
    int val;
    TreeNode *left;
    TreeNode *right;
    TreeNode(int x) : val(x), left(NULL), right(NULL) {}
    TreeNode(int x, TreeNode *l, TreeNode *r) : val(x), left(l), right(r) {}
};
int maxi = INT_MIN;
int dfs(TreeNode *node)
{
    if (!node)
        return 0;
    int ans = node->val;
    int left = dfs(node->left);
    int right = dfs(node->right);
    int t, n, l, r;
    t = ans + left + right;
    n = ans;
    l = ans + left;
    r = ans + right;
    maxi = max(maxi, n);
    maxi = max(maxi, l);
    maxi = max(maxi, r);
    maxi = max(maxi, t);

    return max(n, max(l, r));
}
