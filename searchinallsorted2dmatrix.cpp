void solve()
{
    vvi v = {{1, 3, 5, 7}, {10, 11, 16, 20}, {23, 30, 34, 60}};
    int n = size(v);
    int m = size(v[0]);
    int i = 0;
    int j = (n - 1) * m + (m - 1);
    int t = 13;

    deb2(i, j);
    while (i <= j)
    {
        int mid = (i + j) / 2;
        int r = 0;
        if (mid >= m)
            r = mid / m;
        int c = mid;
        if (mid >= m)
            c = mid % m;
        deb3(mid, r, c);
        if (t == v[r][c])
        {
            debline("yes");
            return;
        }
        else if (t > v[r][c])
        {
            i = mid + 1;
        }
        else
        {
            j = mid - 1;
        }
    }
    debline("none");
}

